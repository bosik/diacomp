package org.bosik.diacomp.core.services.analyze.entities;

public class TimePoint
{
	private int		time;
	private double	value;

	public int getTime()
	{
		return time;
	}

	public void setTime(int time)
	{
		this.time = time;
	}

	public double getValue()
	{
		return value;
	}

	public void setValue(double value)
	{
		this.value = value;
	}
}
